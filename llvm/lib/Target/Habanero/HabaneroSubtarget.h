#ifndef HABANEROSUBTARGET_H
#define HABANEROSUBTARGET_H

#include "llvm/CodeGen/SelectionDAGTargetInfo.h"
#include "llvm/CodeGen/TargetSubtargetInfo.h"

#include "HabaneroFrameLowering.h"
#include "HabaneroISelLowering.h"
#include "HabaneroInstrInfo.h"

#include "Habanero.h"

#define GET_SUBTARGETINFO_HEADER
#include "HabaneroGenSubtargetInfo.inc"

namespace llvm {

class HabaneroSubtarget : public HabaneroGenSubtargetInfo {
  virtual void anchor() {}

  HabaneroRegisterInfo RegInfo;
  HabaneroInstrInfo InstrInfo;
  HabaneroFrameLowering FrameLowering;
  HabaneroTargetLowering TLInfo;
  SelectionDAGTargetInfo TSInfo;

public:
  HabaneroSubtarget(const Triple &TT, const StringRef &CPU, const StringRef &FS,
                const TargetMachine &TM);

  /// ParseSubtargetFeatures - Parses features string setting specified
  /// subtarget options. Definition of function is auto generated by tblgen.
  void ParseSubtargetFeatures(StringRef CPU, StringRef TuneCPU, StringRef FS);

  const HabaneroInstrInfo *getInstrInfo() const override { return &InstrInfo; }
  const HabaneroFrameLowering *getFrameLowering() const override {
    return &FrameLowering;
  }
  const HabaneroTargetLowering *getTargetLowering() const override {
    return &TLInfo;
  }
  const HabaneroRegisterInfo *getRegisterInfo() const override { return &RegInfo; }
  const SelectionDAGTargetInfo *getSelectionDAGInfo() const override {
    return &TSInfo;
  }
};

} // namespace llvm

#endif // HABANEROSUBTARGET_H
